// Tell the computer to remember something
// Declaring & assigning
var name = "Alex"; // DataType String
var id = 599603; // DataType Number
var alive = true; // DataType Boolean
var xyz;
var empty = null;

// Updating values
// Assigning
alive = false;
id = 7;
name = "John";

// Print values
console.log(name); // "Alex" overriden by "John"
console.log(xyz); // undefined - we have not assigned a value yet
console.log(empty); // null - an empty value, or 'nothing'
// Check type
console.log(typeof name); // "string"
