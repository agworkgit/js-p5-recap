/* 
Notes:
- An example is the 'typeof' operator which gives back the type of the value you give it.
- Operators that use two balues are 'binary' operators, while those that use only one value are called 'unary' operators.
- The minus sign can be used as both a 'binary' or 'unary' operator.
*/

console.log(typeof 4.5); // -> number
console.log(typeof "x"); // -> string
console.log(-(10 - 2)); // -> -8
